#include <unistd.h>

int	main(void)
{
	int		i;
	char	n;

	i = 1;
	while (i >= 1 && i <= 100)
	{
		if (i % 3 == 0)
			write (1, &"fizz", 4);
		if (i % 5 == 0)
			write (1, &"buzz", 4);
		if ((i % 3 != 0 && i % 5 != 0) && i < 100)
		{
			if (i > 10)
			{
				n = i / 10 + '0';
				write (1, &n, 1);
				n = i % 10 + '0';
				write (1, &n, 1);
			}
			if (i >= 1 && i <= 9)
			{
				n = i + '0';
				write(1, &n, 1);
			}
		}
		write (1, &"\n", 1);
		i++;
	}
	return (0);
}